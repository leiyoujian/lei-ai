# 计算城市信息
def compute_city(item_iter, model, city_map):
    compute_field = {
        "company_name": 2,
        "address": 1.5,
        "reg_authority": 3,
    }
    city_sore = {}
    for field_item in compute_field.keys():
        if model == 0:
            ns_list = __find_all_provinces_city(item_iter.get(field_item), item_iter.get("city"),
                                                item_iter.get("province"))
        else:
            ns_list = __find_all_provinces_city_b(item_iter.get(field_item), item_iter.get("city"),
                                                  item_iter.get("province"), city_map)

        if ns_list:
            for ns in ns_list:
                # print("ns:", ns)
                ns_key = "{}_{}".format(ns.get("province"), ns.get("city"))
                if ns_key not in city_sore.keys():
                    city_sore[ns_key] = compute_field.get(field_item) * ns.get("score")
                else:
                    city_sore[ns_key] = city_sore[ns_key] + compute_field.get(field_item) * ns.get("score")
    city_sore_list = sorted(city_sore.items(), key=lambda x: x[1], reverse=True)
    # print("city_sore:", city_sore)
    if city_sore_list:
        province_city = city_sore_list[0][0].split("_")
        choose_city_score = city_sore_list[0][1]
        # print("province_city:", province_city)
        return province_city[0], province_city[1], choose_city_score, city_sore
    else:
        return False, False, False, False



