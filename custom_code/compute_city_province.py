# 推测 当 find_city 找到不对应的city的时候，从第三级中推测，看是否是第三级
def __surmise_area_city(city, org_city, org_province, **kwargs):
    '''
    查看是否第三级城市，也就是县，直辖区，县级市，返回对应的二级城市，如果有冲突，则原样返回
    :param area:
    :param org_province:
    :return:
    '''
    if not isinstance(city, str):
        city = ""
    if not isinstance(org_province, str):
        org_province = ""
    # 可能匹配的路径
    match_area_path_list = []
    new_city_province = ()
    if city in AREA_PATH.keys():
        match_area_list_tmp = AREA_PATH.get(city)
        for area_item in match_area_list_tmp:
            match_area_path_list.append((city, area_item[1], area_item[0], 5.0))
    else:
        # 去掉结尾的关键词再猜
        for extend in AREA_EXTEND:
            # print("city:",city,"extend:",extend)
            city = re.sub(re.compile(extend + "$"), '', city)
        if len(city) > 1:
            for extend in AREA_EXTEND:
                _city = city + extend
                if _city in AREA_PATH.keys():
                    match_area_list_tmp = AREA_PATH.get(_city)
                    for area_item in match_area_list_tmp:
                        match_score = 1.5
                        if area_item[1] == org_province:
                            match_score = 2.5
                        match_area_path_list.append((_city, area_item[1], area_item[0], match_score))
    # print("city:", city, " province:", org_province, " match_area_path_list:", match_area_path_list)
    if match_area_path_list:
        # 只有一个匹配到
        if len(match_area_path_list) == 1:
            match_area_path = match_area_path_list[0]
            _match_area_score= match_area_path[3]
            if _match_area_score < 3.5:
                _match_area_score = 3.5
            new_city_province = (match_area_path[0], match_area_path[1], match_area_path[2], _match_area_score)
        # 多个匹配到，但没有省份信息，暂时无法处理，原样返回 todo 预留 **kwargs辅助推测
        elif len(match_area_path_list) > 1 and not org_province:
            new_city_province = (city, org_province, org_city, 0)
        # 多个匹配，有省份信息，todo 预留 **kwargs辅助推测
        elif len(match_area_path_list) > 1 and org_province:
            find_cnt = 0
            for area_path_item in match_area_path_list:
                if org_province == area_path_item[1] and org_city == area_path_item[2]:
                    find_cnt = 1
                    new_city_province = (area_path_item[0], area_path_item[1], area_path_item[2], area_path_item[3])
                    break
                if org_province == area_path_item[1]:
                    find_cnt += 1
                    new_city_province = (area_path_item[0], area_path_item[1], area_path_item[2], area_path_item[3])
            # 多个匹配到，有省份信息，暂时无法处理，原样返回
            if find_cnt > 1:
                new_city_province = (city, org_province, org_city, 1)
            elif find_cnt == 0:
                # 一个也没匹配到，原样返回
                new_city_province = (city, org_province, org_city, 0.1)
    # print("new_city_province:", new_city_province)
    return new_city_province


# 返回字符串中的所有省市信息
def __find_all_provinces_city(val, org_city, org_province):
    '''

    :param val:
    :return:  省，市
    '''
    if not val:
        return None
    val = val.strip()
    seg_list = pseg.lcut(val)
    # print("val:", val, " seg_list:", seg_list)
    ns_list = []
    for ns in seg_list:
        if ns.flag == "ns":
            org_val = ns.word
            if len(org_val) < 2:
                continue
            # 阻止成为第三级
            is_stop = False
            org_val_index = val.index(org_val)
            org_val_len = len(org_val)
            if org_val not in ALL_CITYS:
                for c in CITY_STOP.keys():
                    stop_index = 0
                    try:
                        stop_index = val.index(c, org_val_index)
                    except ValueError:
                        stop_index = 0
                    _distance = CITY_STOP[c]
                    if org_val_index == 0 and _distance >= 2:
                        _distance -= 1
                    if stop_index and stop_index - (org_val_index + org_val_len) < _distance:
                        is_stop = True
                        break
            if is_stop:
                # print("is stop")
                continue
            new_val = __completion(org_val, "city", "null")
            new_province = __completion(org_val, "province", "null")
            # score
            # 6 第二级城市匹配上
            # 5 第三级城市匹配上
            # 1.5 第三级名称推算成功，如果省份相同，则为2.5分
            # 1 第三级名称未推算出,只是提取到了疑似地址

            if new_val:
                _province = __find_province(new_val)
                ns_list.append({"province": _province, "city": new_val, "score": 6.0, "ns": ns})
            elif new_province and not new_val:
                ns_list.append({"province": new_province, "city": "", "score": 0.0, "ns": ns})
            else:
                # 是否第三级
                surmise_set = __surmise_area_city(org_val, org_city, org_province)
                # print("surmise_set：", surmise_set)

                if surmise_set:
                    _find_city = surmise_set[0]
                    _province = surmise_set[1]
                    _city = surmise_set[2]
                    _score = surmise_set[3]
                    # 如果省的相关信息出现过，权重提升一倍
                    _sort_province = _province
                    for _sort_extend in ("省", "市", "县", "自治区"):
                        _sort_province = _sort_province.replace(_sort_extend, "")
                    if _province and _province in val or _sort_province in val:
                        _score = _score * 2

                    if _find_city in CITY_JOIN.keys() and _city != org_city:
                        for c_join in CITY_JOIN.get(_find_city):
                            if c_join in val:
                                ns_list.append(
                                    {"province": _province, "city": _city, "score": _score, "ns": ns})
                            break
                    else:
                        ns_list.append(
                            {"province": _province, "city": _city, "score": _score, "ns": ns})

    return ns_list

