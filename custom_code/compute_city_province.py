# 自定义词库 ，并返回全国地区词
def __add_jieba_words():
    jie_words = set()
    is_addword = len(jieba.user_word_tag_tab) == 0
    area_path_sort = sorted(AREA_PATH.items(), key=lambda x: len(x[0]), reverse=False)
    for c_item in area_path_sort:
        c = c_item[0]
        jie_words.add(c)
        for _extend in AREA_EXTEND:
            if _extend in c:
                _city = re.sub(re.compile(_extend + "$"), '', c)
                jie_words.add(_city)
                if len(_city) > 1 and is_addword:
                    jieba.add_word(_city, freq=50, tag="ns")
        if is_addword:
            jieba.add_word(c, freq=200 + len(_city), tag="ns")

    for c in ALL_CITYS:
        for _extend in CITY_EXTEND:
            if _extend in c:
                _city = re.sub(re.compile(_extend + "$"), '', c)
                jie_words.add(_city)
                if len(_city) > 1 and is_addword:
                    jieba.add_word(_city, freq=900, tag="ns")
        jie_words.add(c)
        if is_addword:
            jieba.add_word(c, freq=910, tag="ns")

    for c in ALL_PROVINCES:
        for _extend in ("省", "自治区"):
            if _extend in c:
                _city = re.sub(re.compile(_extend + "$"), '', c)
                jie_words.add(_city)
                if len(_city) > 1 and is_addword:
                    jieba.add_word(_city, freq=1000, tag="ns")
        jie_words.add(c)
        if is_addword:
            jieba.add_word(c, freq=1100, tag="ns")
    if is_addword:
        for _extend in CITY_EXTEND:
            jieba.del_word(_extend)
        for _extend in AREA_EXTEND:
            jieba.del_word(_extend)
    return jie_words


# 补全省市
def __completion(pre_str=None, str_type='city', def_val="org"):
    """
    补全城市名，或省份名
    :param pre_str: string,所传的城市名
    :param str_type: 判断是补全城市还是省份
    :param def_val: 如果没有匹配到返回值，org表示原样返回，否则返回空
    :return:string
    """
    if not pre_str:
        return ""
    if not isinstance(pre_str, str):
        raise Exception("pre_str 应该为string类型！")
    try:
        if 'city' in str_type:
            if pre_str in ALL_CITYS:
                return pre_str
            for extend in CITY_EXTEND:
                if pre_str + extend in ALL_CITYS:
                    return pre_str + extend
            if def_val == "org":
                return pre_str
            else:
                return ""

        if 'province' in str_type:
            if pre_str in ALL_PROVINCES:
                return pre_str
            # 普通的少了省
            elif pre_str + "省" in ALL_PROVINCES:
                return pre_str + "省"
            # 直辖市
            elif pre_str + "市" in ALL_PROVINCES:
                return pre_str + "市"
            # 自治区
            elif pre_str + "特别行政区" in ALL_PROVINCES:
                return pre_str + "特别行政区"
            elif pre_str in ("内蒙", "内蒙古"):
                return "内蒙古自治区"
            elif pre_str in ("广西", "广西壮族"):
                return "广西壮族自治区"
            elif pre_str in ("西藏"):
                return "西藏自治区"
            elif pre_str in ("宁夏", "宁夏回族"):
                return "宁夏回族自治区"
            elif pre_str in ("新疆", "新疆维吾尔"):
                return "新疆维吾尔自治区"
            else:
                if def_val == "org":
                    return pre_str
                else:
                    return ""
    except:
        return ""


